var color = 'yellow';
switch (color) {
  case 'blue':
    console.log("color is blue");
    break;
  case 'red':
    console.log("color is sky");
    break;
  case 'green':
    console.log('color is green');
    break;
  case 'yellow':
    console.log('color is yellow');
    break;
  default:
    console.log("color is orange");

}